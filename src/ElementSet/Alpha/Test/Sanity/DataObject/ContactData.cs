using System;

using Spring2.Core.Types;

namespace Golf.Tournament.DataObject {
    public class ContactData : Spring2.Core.DataObject.DataObject {

	private StringType name = StringType.DEFAULT;
	private StringType phone = StringType.DEFAULT;
	private StringType email = StringType.DEFAULT;

	public static readonly String NAME = "Name";
	public static readonly String PHONE = "Phone";
	public static readonly String EMAIL = "Email";

	public StringType Name {
	    get { return this.name; }
	    set { this.name = value; }
	}

	public StringType Phone {
	    get { return this.phone; }
	    set { this.phone = value; }
	}

	public StringType Email {
	    get { return this.email; }
	    set { this.email = value; }
	}
    }
}
