using System;

using Spring2.Core.Types;

namespace Golf.Tournament.DataObject {
    public class OrganizationData : Spring2.Core.DataObject.DataObject {

	private IdType organizationId = IdType.DEFAULT;
	private StringType name = StringType.DEFAULT;

	public static readonly String ORGANIZATIONID = "OrganizationId";
	public static readonly String NAME = "Name";

	public IdType OrganizationId {
	    get { return this.organizationId; }
	    set { this.organizationId = value; }
	}

	public StringType Name {
	    get { return this.name; }
	    set { this.name = value; }
	}
    }
}
